fun main(){
	var name : String = "Ramesh"		//String
	var age : Int = 22			//Integer
	var accBalance : Double = 2300.56	//Double
	var letter : Char = 'R'			//Character
	var genderMale : Boolean = true		//Boolean
	var price : Float = 2.34F		// Float
	
	val number : Int?		//to create null value add ? to the end
	number = 10			//

	print("Name is $name")
	print("\nAge is $age")
	print("\nAccount Balance is $accBalance")
	print("\nMy Letter id $letter")
	print("\nIs the gender is male $genderMale")
	print("\nPrice is $price")

	print("\n\nEmpty Variable $number")	
}